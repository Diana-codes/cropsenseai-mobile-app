export const getDistricts = (province) => {
  if (!province || !rwandaLocations[province]) return [];
  return Object.keys(rwandaLocations[province].districts);
};

export const getSectors = (province, district) => {
  if (!province || !district || !rwandaLocations[province]?.districts[district]) return [];
  const districtData = rwandaLocations[province].districts[district];
  if (districtData.sectors && typeof districtData.sectors === 'object') {
    return Object.keys(districtData.sectors);
  }
  return districtData.sectors || [];
};

export const getCells = (province, district, sector) => {
  if (!province || !district || !sector) return [];
  const sectorData = rwandaLocations[province]?.districts[district]?.sectors;
  if (sectorData && typeof sectorData === 'object' && sectorData[sector]?.cells) {
    return Object.keys(sectorData[sector].cells);
  }
  return [];
};

export const getVillages = (province, district, sector, cell) => {
  if (!province || !district || !sector || !cell) return [];
  const cellData = rwandaLocations[province]?.districts[district]?.sectors?.[sector]?.cells;
  if (cellData && cellData[cell]) {
    return cellData[cell];
  }
  return [];
};
